// 9)	WAP to print N even numbers.
import java.util.*;

public class P9 {
    public static void main(String[] args) {
        System.out.println("Enter a number : ");
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i = 0;
        while (i <= n) {
            System.out.println(i);
            i++;
            i++;

        }
    }
}
